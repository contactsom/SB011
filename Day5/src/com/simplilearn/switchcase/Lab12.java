package com.simplilearn.switchcase;

public class Lab12 {

	public static void main(String[] args) {
		
		String country = "IND";
		
		switch(country) {
		
			case "AFG":
				System.out.println("I am at country Afghanistan ");
				break;
			case "ALB":
				System.out.println("I am at country Albania ");
				break;
			case "DZA":
				System.out.println("I am at country Algeria ");
				break;
			case "IND":
				System.out.println("I am at country INDIA ");
				break;
			default:
				System.out.println("I am default country");
			
			
		}

	}

}
