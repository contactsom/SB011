package com.simplilearn.switchcase;

public class Lab11 {

	public static void main(String[] args) {
		
		char alphabet = 'A';
		
		switch(alphabet) {
		
			case 'A':
				System.out.println("A for Apple ");
				break;
			case 'B':
				System.out.println("B for Book ");
				break;
			case 'C':
				System.out.println("C for Cat ");
				break;
			case 'D':
				System.out.println("D for Dog ");
				break;
			default:
				System.out.println("I am default alphabet");
			
			
		}

	}

}
