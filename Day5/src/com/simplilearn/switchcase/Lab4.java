package com.simplilearn.switchcase;

public class Lab4 {

	public static void main(String[] args) {
		
		int x = 1;
		
		switch(x) {
		
			case 3:
				System.out.println("I am 3 ");
			case 0:
				System.out.println("I am 0 ");
			case 2:
				System.out.println("I am 2 ");
			case 1:
				System.out.println("I am 1 ");
			
		}

	}

}
