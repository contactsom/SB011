package com.simplilearn.literals;

public class EscapeLiterals {

	public static void main(String[] args) {
		
			System.out.println("Hello!!!");
			System.out.println("Welcome to Simplilearn");
			
			System.out.println("******************************");
			
			System.out.print("Hello!!!");
			System.out.print("Welcome to Simplilearn");
			
			System.out.println();
			
			System.out.println("******************************");
			
			System.out.print("Hello!!! \n");
			System.out.print("Welcome to Simplilearn \n");

	}

}
