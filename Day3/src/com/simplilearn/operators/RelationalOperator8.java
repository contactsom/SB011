package com.simplilearn.operators;

public class RelationalOperator8 {

	public static void main(String[] args) {
	
		int a = 30;
		int b = 20;
	
		int c = 50;
		int d = 50;
		
		System.out.println(a != b);// true or 1
		
		System.out.println(c != d);//false or 0
		
		System.out.println("***********");
		System.out.println( (a!=b) & (c!=d));
		
		// AND gate = Multiply
		// OR gate = Add
		// true & false
		// 1 & 0
		// 0
		// false
	
		
	}
}
