package com.simplilearn.operators;

public class RelationalOperator7 {

	public static void main(String[] args) {
	
		int a = 30;
		int b = 20;
	
		int c = 50;
		int d = 50;
		
		System.out.println(a != b);// true or 1
		
		System.out.println(c != d);//false or 0
		
		System.out.println("***********");
		System.out.println( (a!=b) | (c!=d));
		
		// AND gate = Multiply
		// OR gate = Add
		// true or false
		// 1 or 0
		// 1
		// true
	
		
	}
}
